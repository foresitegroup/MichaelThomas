    &bull; Conduct targeted research into companies and sectors to find those with relevant skill-sets and qualifications, supplementing our extensive knowledge of and relationships with middle to senior level managers and executives across industries and functions.<br>
    <br>

    &bull; Develop a list of candidates best qualified for the role, with the goal of providing real opinions for client consideration.<br>
    <br>

    &bull; Talk with third-party contacts to identify and qualify prospects; that align to our approach, this preliminary referencing work makes for more productive long-list discussions.

