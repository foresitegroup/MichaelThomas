    &bull; Approach potential candidates to test their sincere interest in the specific role, drawing on our insights about candidates interests and aspirations to persuade strong prospects to consider new opportunities. Our reputation and experience is attracting top candidates who normally are not looking to make a change. We develop an in depth dialog and ultimately begin to get those candidate we deem a "perfect fit" into the interview process.<br>
    <br>

    &bull; Conduct rigorous competency-based interviews with candidates based on the proven skills, knowledge, abilities and aptitudes outlined in the position specification.<br>
    <br>

    &bull; Develop a short list of candidates with the qualifications and interest in the position.
